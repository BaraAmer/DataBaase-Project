package application;

public class Emp {
	private int eid;
	private String ename;
	private String specialization;
	private String address;
	private String birthdate;
	private int evaluation;
	private String gender;
	private int nofexperiancess;
	private int salary;
	private int taxvalue;
	private String dayoffice;
	private int dnumber;
	private String phone1;
	private String phone2;
	
	
	public Emp(int eid, String ename, String specialization, String address, String birthdate, int evaluation,
			String gender, int nofexperiancess, int salary, int taxvalue, String dayoffice, int dnumber, String phone1,
			String phone2) {
		super();
		this.eid = eid;
		this.ename = ename;
		this.specialization = specialization;
		this.address = address;
		this.birthdate = birthdate;
		this.evaluation = evaluation;
		this.gender = gender;
		this.nofexperiancess = nofexperiancess;
		this.salary = salary;
		this.taxvalue = taxvalue;
		this.dayoffice = dayoffice;
		this.dnumber = dnumber;
		this.phone1 = phone1;
		this.phone2 = phone2;
	}


	public int getEid() {
		return eid;
	}


	public void setEid(int eid) {
		this.eid = eid;
	}


	public String getEname() {
		return ename;
	}


	public void setEname(String ename) {
		this.ename = ename;
	}


	public String getSpecialization() {
		return specialization;
	}


	public void setSpecialization(String specialization) {
		this.specialization = specialization;
	}


	public String getAddress() {
		return address;
	}


	public void setAddress(String address) {
		this.address = address;
	}


	public String getBirthdate() {
		return birthdate;
	}


	public void setBirthdate(String birthdate) {
		this.birthdate = birthdate;
	}


	public int getEvaluation() {
		return evaluation;
	}


	public void setEvaluation(int evaluation) {
		this.evaluation = evaluation;
	}


	public String getGender() {
		return gender;
	}


	public void setGender(String gender) {
		this.gender = gender;
	}


	public int getNofexperiancess() {
		return nofexperiancess;
	}


	public void setNofexperiancess(int nofexperiancess) {
		this.nofexperiancess = nofexperiancess;
	}


	public int getSalary() {
		return salary;
	}


	public void setSalary(int salary) {
		this.salary = salary;
	}


	public int getTaxvalue() {
		return taxvalue;
	}


	public void setTaxvalue(int taxvalue) {
		this.taxvalue = taxvalue;
	}


	public String getDayoffice() {
		return dayoffice;
	}


	public void setDayoffice(String dayoffice) {
		this.dayoffice = dayoffice;
	}


	public int getDnumber() {
		return dnumber;
	}


	public void setDnumber(int dnumber) {
		this.dnumber = dnumber;
	}


	public String getPhone1() {
		return phone1;
	}


	public void setPhone1(String phone1) {
		this.phone1 = phone1;
	}


	public String getPhone2() {
		return phone2;
	}


	public void setPhone2(String phone2) {
		this.phone2 = phone2;
	}
	
	
	
	
	
	

}





