package application;

public class clients {
		 private int cid;
		 private String cname ;
		 private int bill;

		public clients(int cid, String cname, int bill) {
			super();
			this.cid = cid;
			this.cname = cname;
			this.bill = bill;
		}

		public int getCid() {
			return cid;
		}

		public void setCid(int cid) {
			this.cid = cid;
		}

		public String getCname() {
			return cname;
		}

		public void setCname(String cname) {
			this.cname = cname;
		}

		public int getBill() {
			return bill;
		}

		public void setBill(int bill) {
			this.bill = bill;
		}
		
		
		 
		 
		 
		 
		 
		 
  }

